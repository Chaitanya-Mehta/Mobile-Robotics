function Y=InPosHex(Theta)
l0=2.5;%cm
l1=7.4;%cm
l2=11.4;%cm
lc=l0;
lf=l1;
lt=l2;

t2=[Theta.t1(2);...
Theta.t2(2);...
Theta.t3(2);...
Theta.t4(2);...
Theta.t5(2);...
Theta.t6(2)];
t3=[Theta.t1(3);...
Theta.t2(3);...
Theta.t3(3);...
Theta.t4(3);...
Theta.t5(3);...
Theta.t6(3)];

PP=lf*sin(t2)+lt*sin(t3+t2);
Y=mean(PP);